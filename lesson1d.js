// String data type
// A string is a series/sequence of characters that are enclosed inside quotation markers

let name = "Audrey"
console.log("The name is", name)
console.log(typeof(name))

// Floating point: This is number with a decimal
let quantity = 35.5
console.log("The quantity of sugar bought is:", quantity)
console.log(typeof(quantity))

// Bolean: It returns true or false
let isAdult = true
let isRegistered = false
console.log("Is Audrey an Adult?", isAdult)
console.log("Is she registered as a voter?", isRegistered)

// Undefined : This is a data type that has been declaired  but there is no value that has been assigned
let county;
console.log("The county is", county)

// Null data type : It is a data type that contains null values
let username = null
console.log("What is the username?", username)


